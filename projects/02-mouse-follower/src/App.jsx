import { useState } from "react";
import { useEffect } from "react";

const FollowMouse = () => {
  const [enabled, setEnabled] = useState(false);
  const [position, setPosition] = useState({ x: 0, y: 0 });

  useEffect(() => {
    const handleMove = (event) => {
      const { clientX, clientY } = event;
      setPosition({ x: clientX, y: clientY });
    };

    if (enabled) {
      // Si activo el seguir cursor y despuÃ©s lo desactivo hay que hacer un cleanEffect
      window, addEventListener("pointermove", handleMove);
    }

    // Cleanup
    // Se ejecuta cuando se desmonta el componente
    // Y cuando cambien las dependencias, antes de ejecutar el efecto de nuevo
    return () => {
      window.removeEventListener("pointermove", handleMove);
    }; // Cuando se ejecuta esto? Explicado 1 hora y 32 min de video
  }, [enabled]);

  return (
    <>
      <div
        style={{
          position: "absolute",
          backgroundColor: "rgba(0, 0, 0, 0.5)",
          border: "1px solid #fff",
          borderRadius: "50%",
          opacity: 0.8,
          pointerEvents: "none",
          left: -25,
          top: -25,
          width: 50,
          height: 50,
          transform: `translate(${position.x}px, ${position.y}px)`,
        }}
      />
      <button onClick={() => setEnabled(!enabled)}>
        {enabled ? "Desactivar" : "Activar"} seguir puntero
      </button>
    </>
  );
};

function App() {
  return (
    <main>
      <FollowMouse />
    </main>
  );
}

export default App;
